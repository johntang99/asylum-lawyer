export function getArticleBySlug(slug: string): ArticleData | undefined {
  return ALL_ARTICLES.find((a) => a.slug === slug);
}

export function getRelatedArticles(slugs: string[]): ArticleData[] {
  return slugs
    .map((s) => ALL_ARTICLES.find((a) => a.slug === s))
    .filter((a): a is ArticleData => a !== undefined);
}

export function getArticlesByCategory(category: string): ArticleData[] {
  if (category === 'all' || !category) return ALL_ARTICLES;
  return ALL_ARTICLES.filter((a) => a.category === category);
}
